#!/usr/bin/env node
// bundle.mjs — concatenate the engine and one harness config into a single
// self-contained Workflow script (the Workflow runtime has no `import`).
//
//   node tools/bundle.mjs <harness.config.mjs> <out.workflow.mjs>
//
// Limitation by design: configs must be self-contained (no imports).

import { readFileSync, writeFileSync } from 'node:fs'
import { fileURLToPath, pathToFileURL } from 'node:url'
import { dirname, join, resolve } from 'node:path'

const [cfgArg, outArg] = process.argv.slice(2)
if (!cfgArg || !outArg) {
  console.error('usage: node tools/bundle.mjs <harness.config.mjs> <out.workflow.mjs>')
  process.exit(1)
}
const cfgPath = resolve(cfgArg)
const outPath = resolve(outArg)
const enginePath = join(dirname(fileURLToPath(import.meta.url)), '..', 'engine', 'dual_agent_loop.mjs')

const config = (await import(pathToFileURL(cfgPath).href)).default
if (!config?.name) { console.error('config has no default export with a name'); process.exit(1) }

// Refuse to bundle a config still wearing its TODOs. The conformance gate
// already refuses one; a bundler that shrugs and emits a runnable workflow
// anyway hands a newcomer a harness that grades nothing and says VALIDATED.
// The two refusals must agree, or the softer one is the only one that matters.
const TODO = /\bTODO\b/i
const unfilled = []
if (TODO.test(String(config.name))) unfilled.push('name')
for (const k of ['metric', 'gate', 'hardConstraint']) {
  if (TODO.test(String(config.objective?.[k] ?? ''))) unfilled.push(`objective.${k}`)
}
if (TODO.test(String(config.heldApartRule ?? ''))) unfilled.push('heldApartRule')
for (const f of config.finders || []) {
  if (TODO.test(String(f?.lens ?? ''))) unfilled.push(`finders lens "${f.lens}"`)
  if (TODO.test(String(f?.hint ?? ''))) unfilled.push(`finders hint for "${f?.lens}"`)
}
if (unfilled.length) {
  console.error(`refusing to bundle: ${unfilled.length} field(s) still contain "TODO":`)
  for (const u of unfilled) console.error('  - ' + u)
  console.error('\nFill them, then run `node engine/conform.mjs <instance>` until it PASSES.')
  console.error('An unfilled harness runs, grades nothing, and reports VALIDATED.')
  process.exit(1)
}

// Inline the Gap tool (engine/gap.mjs) and the one pure helper it shares from
// kappa.mjs (canonicalJson). The Workflow runtime has no `import` and no
// node:crypto, so gap.mjs carries a pure-JS SHA-256 and imports only
// canonicalJson — both inlined here, imports stripped. The engine's
// `import ... from './gap.mjs'` line is removed; its symbols are now in scope.
const engineDir = join(dirname(fileURLToPath(import.meta.url)), '..', 'engine')
const kappaSrc = readFileSync(join(dirname(fileURLToPath(import.meta.url)), '..', 'tools', 'kappa.mjs'), 'utf8')
const canonMatch = kappaSrc.match(/export function canonicalJson[\s\S]*?\n\}/)
if (!canonMatch) { console.error('bundle: could not extract canonicalJson from kappa.mjs'); process.exit(1) }
const canonSrc = canonMatch[0].replace(/^export /, '')

const gapSrc = readFileSync(join(engineDir, 'gap.mjs'), 'utf8')
  .replace(/^import[^\n]*\n/gm, '')   // drop imports (canonicalJson inlined above)
  .replace(/^export /gm, '')

const engineSrc = readFileSync(enginePath, 'utf8')
  .replace(/^import[^\n]*\n/gm, '')   // drop the gap.mjs import; symbols inlined
  .replace(/^export /gm, '')
const configSrc = readFileSync(cfgPath, 'utf8')
  .replace(/^export default/m, 'const config =')
if (/^\s*import /m.test(configSrc)) { console.error('config must be self-contained: no imports'); process.exit(1) }

const meta = `export const meta = {
  name: ${JSON.stringify(config.name)},
  description: ${JSON.stringify(`soulbis⊥soulbae dual-agent harness round on ${config.name} (Measure → Propose → Hold-apart → Assay → Critic → Chronicle)`)},
  phases: [
    { title: 'Measure', detail: 'sync the frontier' },
    { title: 'Propose', detail: 'soulbae 🧙 — parallel lenses, blind' },
    { title: 'Hold-apart', detail: 'the Gap ⿻ derives held-out witnesses' },
    { title: 'Assay', detail: 'soulbis ⚔️ runs the full gate' },
    { title: 'Critic', detail: 'classifies the round' },
    { title: 'Chronicle', detail: 'drafts the record' },
  ],
}
`

const trailer = `
// ---- run (Workflow globals: agent, parallel, pipeline, phase, log, args) ----
const A = (typeof args === 'string') ? JSON.parse(args) : (args || {})
if (!A.repo) throw new Error('args.repo required (absolute path to this harness instance); args.root = the skeleton clone holding GROUND_RULES.md/TRUSTS.md/seats/, if the instance does not carry its own copies')
return await runHarness(config, { agent, parallel, pipeline, phase, log }, A)
`

const banner = `// GENERATED by tools/bundle.mjs from ${cfgArg.replace(/\\/g, '/')} — do not hand-edit; edit the config and rebundle.\n// Engine: engine/dual_agent_loop.mjs · Contract: SEAT_CONTRACT.md · Constitution: TRUSTS.md\n`

const gapInline = `// ---- inlined: kappa.canonicalJson + engine/gap.mjs (sandbox-safe, no import/node:crypto) ----\n${canonSrc}\n${gapSrc}\n`
writeFileSync(outPath, meta + banner + '\n' + gapInline + '\n' + engineSrc + '\n' + configSrc + trailer)
console.log(`bundled ${config.name} -> ${outPath}`)
