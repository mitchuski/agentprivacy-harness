#!/usr/bin/env node
// bundle.mjs — concatenate the engine and one harness config into a single
// self-contained Workflow script (the Workflow runtime has no `import`).
//
//   node tools/bundle.mjs <harness.config.mjs> <out.workflow.mjs>
//
// Limitation by design: configs must be self-contained (no imports).

import { readFileSync, writeFileSync } from 'node:fs'
import { fileURLToPath, pathToFileURL } from 'node:url'
import { dirname, join, resolve } from 'node:path'

const [cfgArg, outArg] = process.argv.slice(2)
if (!cfgArg || !outArg) {
  console.error('usage: node tools/bundle.mjs <harness.config.mjs> <out.workflow.mjs>')
  process.exit(1)
}
const cfgPath = resolve(cfgArg)
const outPath = resolve(outArg)
const enginePath = join(dirname(fileURLToPath(import.meta.url)), '..', 'engine', 'dual_agent_loop.mjs')

const config = (await import(pathToFileURL(cfgPath).href)).default
if (!config?.name) { console.error('config has no default export with a name'); process.exit(1) }

const engineSrc = readFileSync(enginePath, 'utf8')
  .replace(/^export /gm, '')
const configSrc = readFileSync(cfgPath, 'utf8')
  .replace(/^export default/m, 'const config =')
if (/^\s*import /m.test(configSrc)) { console.error('config must be self-contained: no imports'); process.exit(1) }

const meta = `export const meta = {
  name: ${JSON.stringify(config.name)},
  description: ${JSON.stringify(`soulbis⊥soulbae dual-agent harness round on ${config.name} (Measure → Propose → Hold-apart → Assay → Critic → Chronicle)`)},
  phases: [
    { title: 'Measure', detail: 'sync the frontier' },
    { title: 'Propose', detail: 'soulbae 🧙 — parallel lenses, blind' },
    { title: 'Hold-apart', detail: 'the Gap ⿻ derives held-out witnesses' },
    { title: 'Assay', detail: 'soulbis ⚔️ runs the full gate' },
    { title: 'Critic', detail: 'classifies the round' },
    { title: 'Chronicle', detail: 'drafts the record' },
  ],
}
`

const trailer = `
// ---- run (Workflow globals: agent, parallel, pipeline, phase, log, args) ----
const A = (typeof args === 'string') ? JSON.parse(args) : (args || {})
if (!A.repo) throw new Error('args.repo required (absolute path to this harness instance); args.root = the skeleton clone holding GROUND_RULES.md/TRUSTS.md/seats/, if the instance does not carry its own copies')
return await runHarness(config, { agent, parallel, pipeline, phase, log }, A)
`

const banner = `// GENERATED by tools/bundle.mjs from ${cfgArg.replace(/\\/g, '/')} — do not hand-edit; edit the config and rebundle.\n// Engine: engine/dual_agent_loop.mjs · Contract: SEAT_CONTRACT.md · Constitution: TRUSTS.md\n`

writeFileSync(outPath, meta + banner + '\n' + engineSrc + '\n' + configSrc + trailer)
console.log(`bundled ${config.name} -> ${outPath}`)
